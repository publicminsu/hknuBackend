CREATE TABLE ACCOUNT 
(
	Id SERIAL PRIMARY KEY,
	Email TEXT UNIQUE NOT NULL,
	PASSWORD TEXT NOT NULL,
	NICKNAME TEXT UNIQUE NOT NULL,
	PHONE_NUMBER TEXT UNIQUE NOT NULL,
	WISH_LIST TEXT,
	COUPON_LIST TEXT,
	PAYMENT_CARD TEXT
);
CREATE TABLE DELETED_ACCOUNT 
(
	Id SERIAL PRIMARY KEY,
	Email TEXT UNIQUE NOT NULL,
	PHONE_NUMBER TEXT UNIQUE NOT NULL,
	DELETE_TIME TIMESTAMP NOT NULL
);
CREATE TABLE STORE
(
	ID SERIAL PRIMARY KEY,
	ACCOUNT_ID INT REFERENCES ACCOUNT(ID),
	NAME TEXT NOT NULL,
	ADDRESS TEXT,
	INFO TEXT,
	PHONE_NUMBER TEXT,
	CAN_RESERVATION BOOL,
	OPERATING_TIME TEXT,
	IS_OPEN BOOL
);
CREATE TABLE REVIEW
(
	ID SERIAL PRIMARY KEY,
	ACCOUNT_ID INT REFERENCES ACCOUNT(ID),
	STORE_ID INT REFERENCES STORE(ID),
	DETAIL TEXT,
	WRITING_TIME TIMESTAMP NOT NULL,
	RATING INT NOT NULL
);
CREATE TABLE MENU
(
	ID SERIAL PRIMARY KEY,
	STORE_ID INT REFERENCES STORE(ID),
	PHOTO TEXT,
	NAME TEXT NOT NULL,
	PRICE TEXT NOT NULL,
	CATEGORY TEXT
);
CREATE TABLE TABLE_ITEM
(
	ID SERIAL PRIMARY KEY,
	STORE_ID INT REFERENCES STORE(ID),
	NAME TEXT,
	COORD_X TEXT NOT NULL,
	COORD_Y TEXT NOT NULL,
	WIDTH TEXT NOT NULL,
	HEIGHT TEXT NOT NULL,
	PRIVATE_KEY TEXT NOT NULL
);
CREATE TABLE ORDER_ITEM
(
	ID SERIAL PRIMARY KEY,
	ACCOUNT_ID INT REFERENCES ACCOUNT(ID),
	STORE_ID INT REFERENCES STORE(ID),
	TABLE_ID INT REFERENCES TABLE_ITEM(ID),
	ORDER_TIME TIMESTAMP NOT NULL,
	PAYMENT_TIME TIMESTAMP NOT NULL,
	RESERVATION_TIME TIMESTAMP NOT NULL,
	IS_SERVED BOOL NOT NULL
);
CREATE TABLE ORDER_DETAIL
(
	ID SERIAL PRIMARY KEY,
	ORDER_ID INT REFERENCES ORDER_ITEM(ID),
	MENU_ID INT REFERENCES MENU(ID),
	AMOUNT INT NOT NULL
);
CREATE TABLE AUTH
(
	ID SERIAL PRIMARY KEY,
	ACCOUNT_ID INT UNIQUE REFERENCES ACCOUNT(ID),
	REFRESH_TOKEN TEXT
)